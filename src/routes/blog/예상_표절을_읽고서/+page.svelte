표절의 첫번째 가정인 유사성은 확인하기가 쉽다. 텍스트에서 느껴지는 작가의 체취는 생각보다 강력해서 지각있는 독자라면 단번에 알아차리기 때문이다. 하지만 유사성을 확인하기가 어렵다고 느껴지는 이유는, 표절의 시비에 부딪친 작가의 흔한 착각 때문인데 - 자신을 변호할 유일한 방법이 두 텍스트 간에 드러나는 유사성을 부인하는 것뿐이라고 생각하기 때문이다. 따라서 우리는 표절 논쟁에서 유사성의 발견은 일종의 주관적인 인상, 언제든 뒤집힐 수 있는 자의적인 판단이라는 반박을 접하게 되고, 자연스럽게 유사성의 인식이란 객관적이지 못한거다라고 착각하게 된다.
</Paragraph>
